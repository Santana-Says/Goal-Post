import UIKit

extension UIButton {
    func setSelectedColor() {
        backgroundColor = #colorLiteral(red: 0.3417285396, green: 0.7722371817, blue: 0.2946935723, alpha: 1)
    }
    
    func setDeselectedColor() {
        backgroundColor = #colorLiteral(red: 0.3417285396, green: 0.7722371817, blue: 0.2946935723, alpha: 0.507036601)
    }
}
